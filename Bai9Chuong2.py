# Khai báo các biến
i1 = 2
i2 = 5
i3 = -3
d1 = 2.0
d2 = 5.0
d3 = -0.5

# (a) i1 + (i2 * i3)
result_a = i1 + (i2 * i3)  # 2 + (5 * -3) = 2 + (-15) = -13
print("(a) i1 + (i2 * i3):", result_a)

# (b) i1 * (i2 + i3)
result_b = i1 * (i2 + i3)  # 2 * (5 + -3) = 2 * 2 = 4
print("(b) i1 * (i2 + i3):", result_b)

# (c) i1 / (i2 + i3)
result_c = i1 / (i2 + i3)  # 2 / (5 + -3) = 2 / 2 = 1.0
print("(c) i1 / (i2 + i3):", result_c)

# (d) i1 // (i2 + i3)
result_d = i1 // (i2 + i3)  # 2 // (5 + -3) = 2 // 2 = 1
print("(d) i1 // (i2 + i3):", result_d)

# (e) i1 / i2 + i3
result_e = i1 / i2 + i3  # 2 / 5 + -3 = 0.4 + -3 = -2.6
print("(e) i1 / i2 + i3:", result_e)

# (f) i1 // i2 + i3
result_f = i1 // i2 + i3  # 2 // 5 + -3 = 0 + -3 = -3
print("(f) i1 // i2 + i3:", result_f)

# (g) 3 + 4 + 5 / 3
result_g = 3 + 4 + 5 / 3  # 3 + 4 + (5 / 3) = 3 + 4 + 1.666... = 8.666...
print("(g) 3 + 4 + 5 / 3:", result_g)

# (h) 3 + 4 + 5 // 3
result_h = 3 + 4 + 5 // 3  # 3 + 4 + (5 // 3) = 3 + 4 + 1 = 8
print("(h) 3 + 4 + 5 // 3:", result_h)

# (i) (3 + 4 + 5) / 3
result_i = (3 + 4 + 5) / 3  # (3 + 4 + 5) / 3 = 12 / 3 = 4.0
print("(i) (3 + 4 + 5) / 3:", result_i)

# (j) (3 + 4 + 5) // 3
result_j = (3 + 4 + 5) // 3  # (3 + 4 + 5) // 3 = 12 // 3 = 4
print("(j) (3 + 4 + 5) // 3:", result_j)

# (k) d1 + (d2 * d3)
result_k = d1 + (d2 * d3)  # 2.0 + (5.0 * -0.5) = 2.0 + -2.5 = -0.5
print("(k) d1 + (d2 * d3):", result_k)

# (l) d1 + d2 * d3
result_l = d1 + d2 * d3  # 2.0 + 5.0 * -0.5 = 2.0 + -2.5 = -0.5
print("(l) d1 + d2 * d3:", result_l)

# (m) d1 / d2 - d3
result_m = d1 / d2 - d3  # 2.0 / 5.0 - -0.5 = 0.4 + 0.5 = 0.9
print("(m) d1 / d2 - d3:", result_m)

# (n) d1 / (d2 - d3)
result_n = d1 / (d2 - d3)  # 2.0 / (5.0 - -0.5) = 2.0 / 5.5 = 0.363636...
print("(n) d1 / (d2 - d3):", result_n)

# (o) d1 + d2 + d3 / 3
result_o = d1 + d2 + d3 / 3  # 2.0 + 5.0 + (-0.5 / 3) = 2.0 + 5.0 - 0.166... = 6.833...
print("(o) d1 + d2 + d3 / 3:", result_o)

# (p) (d1 + d2 + d3) / 3
result_p = (d1 + d2 + d3) / 3  # (2.0 + 5.0 + -0.5) / 3 = 6.5 / 3 = 2.166...
print("(p) (d1 + d2 + d3) / 3:", result_p)

# (q) d1 + d2 + (d3 / 3)
result_q = d1 + d2 + (d3 / 3)  # 2.0 + 5.0 + (-0.5 / 3) = 2.0 + 5.0 - 0.166... = 6.833...
print("(q) d1 + d2 + (d3 / 3):", result_q)

# (r) 3 * (d1 + d2) * (d1 - d3)
result_r = 3 * (d1 + d2) * (d1 - d3)  # 3 * (2.0 + 5.0) * (2.0 - -0.5) = 3 * 7.0 * 2.5 = 52.5
print("(r) 3 * (d1 + d2) * (d1 - d3):", result_r)
