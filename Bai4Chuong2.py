# Kiểu dữ liệu số (Numeric Types)
x = 10  # int
y = -5  # int
a = 3.14  # float
b = -2.7  # float
c = 3 + 4j  # complex

print("Kiểu int:", x, y)
print("Kiểu float:", a, b)
print("Kiểu complex:", c)

# Kiểu dữ liệu chuỗi (String Type)
name = "John Doe"  # str
greeting = 'Hello, World!'  # str
print("Kiểu str:", name, greeting)
