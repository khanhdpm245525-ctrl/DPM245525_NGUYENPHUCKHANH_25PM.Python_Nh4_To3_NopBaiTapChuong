# Hàm minh họa việc sử dụng ghi chú
def add(a, b):
    """
    Hàm này thực hiện phép cộng hai số a và b.
    a: số đầu tiên
    b: số thứ hai
    Trả về: tổng của a và b
    """
    return a + b  # Trả về kết quả phép cộng

# Gọi hàm và in kết quả
result = add(3, 7)  # Gọi hàm add với a=3, b=7
print(result)  # In kết quả
